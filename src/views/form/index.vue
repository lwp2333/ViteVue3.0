import { NotEmpty, NotSelect, NotRadio, limitStr } from '../../utils/validate'
export default {
  setup() {
    let ruleForm = ref(null)
    const form = reactive({
      name: null,
      region: null,
      date: null,
      publish: null,
      type: [],
      resource: null,
      desc: null
    })
    const setRef = el => {
      ruleForm = el
    }
    onMounted(() => {
      // console.dir(ruleForm)
    })
    const onSubmit = () => {
      ruleForm
        .validate()
        .then(() => {
          console.log('values', form)
        })
        .catch(error => {
          console.log('error', error)
        })
    }
    const resetForm = () => {
      ruleForm.resetFields()
    }
    return {
      labelCol: {
        xl: 4,
        sm: 8
      },
      wrapperCol: {
        xl: 10,
        sm: 16
      },
      ruleForm,
      form,
      rules: {
        name: [NotEmpty('活动名称'), limitStr(3, 5, '活动名称')],
        region: [NotRadio('活动地址')],
        date: [NotRadio('活动时间')],
        type: [NotSelect('活动类型')],
        resource: [NotRadio('活动来源')],
        desc: [NotEmpty('活动描述')]
